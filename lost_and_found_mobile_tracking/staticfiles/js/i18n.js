let currentLang = localStorage.getItem('lang') || 'en';
const originalTexts = new WeakMap();
const originalPlaceholders = new WeakMap();

/**
 * Robust translation for a single node and its descendants
 */
function translateNode(node) {
  if (!node) return;

  // 1. Handle Text Nodes
  if (node.nodeType === Node.TEXT_NODE) {
    const parent = node.parentElement;
    if (parent && (parent.tagName === 'SCRIPT' || parent.tagName === 'STYLE')) return;

    let text = node.textContent;
    let trimmed = text.trim();
    if (!trimmed) return;

    // Remove common punctuation for matching
    let cleanKey = trimmed.replace(/[.,!?]$/, '');
    
    let original = originalTexts.get(node);
    if (original === undefined) {
      original = trimmed;
      originalTexts.set(node, original);
    }

    const dict = translations[currentLang];
    
    // Case-insensitive lookup helper
    function getFromDict(key) {
      if (!dict) return null;
      if (dict[key]) return dict[key];
      for (let k in dict) {
        if (k.toLowerCase() === key.toLowerCase()) return dict[k];
      }
      return null;
    }

    const translated = getFromDict(original) || getFromDict(cleanKey);
    const match = text.match(/^(\s*)(.*?)(\s*)$/);
    const prefix = match ? match[1] : '';
    const suffix = match ? match[3] : '';

    let newText = text;
    if (translated) {
      // If we matched the cleanKey, we need to append the punctuation back
      if (translated === getFromDict(cleanKey) && translated !== getFromDict(original)) {
        const punct = trimmed.slice(cleanKey.length);
        newText = prefix + translated + punct + suffix;
      } else {
        newText = prefix + translated + suffix;
      }
    } else {
      newText = prefix + original + suffix;
    }
    
    if (node.textContent !== newText) {
      node.textContent = newText;
    }
  } 
  
  // 2. Handle Element Nodes
  else if (node.nodeType === Node.ELEMENT_NODE) {
    if (node.tagName === 'SCRIPT' || node.tagName === 'STYLE') return;

    // Handle Placeholders
    if (node.placeholder) {
      let original = originalPlaceholders.get(node);
      if (original === undefined) {
        original = node.placeholder;
        originalPlaceholders.set(node, original);
      }
      const dict = translations[currentLang];
      
      function getFromDict(key) {
        if (!dict) return null;
        if (dict[key]) return dict[key];
        for (let k in dict) {
          if (k.toLowerCase() === key.toLowerCase()) return dict[k];
        }
        return null;
      }

      const translated = getFromDict(original);
      if (translated) {
        node.placeholder = translated;
      } else {
        node.placeholder = original;
      }
    }

    // Recursively translate children
    node.childNodes.forEach(child => translateNode(child));
  }
}

/**
 * Apply translation to the entire document
 */
function applyTranslation() {
  translateNode(document.documentElement);
  
  // Update UI Indicator
  const indicator = document.getElementById('lang-indicator');
  if (indicator) {
    indicator.textContent = currentLang.toUpperCase();
  }
}

/**
 * Mutation Observer to handle dynamic content
 */
const observer = new MutationObserver((mutations) => {
  const nodesToTranslate = new Set();
  mutations.forEach((mutation) => {
    if (mutation.type === 'childList') {
      mutation.addedNodes.forEach((node) => nodesToTranslate.add(node));
    } else if (mutation.type === 'characterData') {
      nodesToTranslate.add(mutation.target);
    }
  });

  if (nodesToTranslate.size > 0) {
    observer.disconnect();
    nodesToTranslate.forEach(node => translateNode(node));
    startObserver();
  }
});

function startObserver() {
  observer.observe(document.documentElement, {
    childList: true,
    subtree: true,
    characterData: true
  });
}

/**
 * Global API
 */
function setLanguage(lang) {
  if (lang === currentLang) return;
  currentLang = lang;
  localStorage.setItem('lang', lang);
  
  // Set cookie for backend access
  document.cookie = "user_lang=" + lang + "; path=/; max-age=" + (365 * 24 * 60 * 60);

  // Re-translate everything
  observer.disconnect();
  applyTranslation();
  startObserver();
  
  // Custom event for components that need to know about language change
  window.dispatchEvent(new CustomEvent('languageChanged', { detail: lang }));
}

// Initialize
document.addEventListener('DOMContentLoaded', () => {
  // Theme initialization
  const theme = localStorage.getItem('theme') || 'dark';
  document.documentElement.setAttribute('data-theme', theme);
  
  // Translation initialization
  applyTranslation();
  startObserver();
});

function toggleTheme() {
  const current = document.documentElement.getAttribute('data-theme') || 'dark';
  const next = current === 'dark' ? 'light' : 'dark';
  document.documentElement.setAttribute('data-theme', next);
  localStorage.setItem('theme', next);
}
